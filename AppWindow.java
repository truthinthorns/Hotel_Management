    public int GetRoomNumber(HotelRoom.RoomType type)
    {
        int x = -999;
        Boolean found = false;
        for(int i = 0; i < MAXROOMS; i++)
        {
            if(!found){
                if(infoVec.get(i).type==type && !infoVec.get(i).filled)
                {
                    x = i+1;
                    found = true;
                    infoVec.get(i).SetFilled(found);
                }
            }
        }
        return x;
    }
    
