    private void CheckCodeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckCodeButtonActionPerformed
        String s = ReservationCodeField.getText();
        if(s.isBlank())
            return;
        Boolean found = false;
        for(HotelRoom h:reservationVec)
        {
            if(!found)
            {
                if(h.getRCode().equals(s))
                {
                    found = true;
                    NameField_CI.setText(h.getCustomerName());
                    NightsComboBox.setSelectedIndex(h.getNumNights()-1);
                    OccupantsComboBox.setSelectedIndex(h.getNumOccupants()-1);
                    RoomTypeComboBox.setSelectedItem(h.getRoomType().toString());
                    tempRoom = h;
                }
            }
        }
        if(!found)
            JOptionPane.showMessageDialog(CheckInPanel,"Reservation code not found!");
    }//GEN-LAST:event_CheckCodeButtonActionPerformed

