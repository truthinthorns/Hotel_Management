    public HotelRoom Search(String s, int n)
    {
        if(s.isBlank())
        {
            for(int i = 0; i < roomsVec.size();i++)
            {
                if(roomsVec.get(i).roomNumber == n)
                {
                    return roomsVec.get(i);
                }
            }
        }
        else if (n==1 && !s.isBlank())
        {
            for(int i = 0; i < roomsVec.size();i++)
            {
                if(roomsVec.get(i).customerName.equalsIgnoreCase(s))
                {
                    return roomsVec.get(i);
                }
            }
        }
        return null;
    }
    
    private static final String ALPHA_NUMERIC_STRING = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
    public static String GenerateRCode(int count) 
    {
    StringBuilder builder = new StringBuilder();
    while (count-- != 0) 
    {
        int character = (int)(Math.random()*ALPHA_NUMERIC_STRING.length());
        builder.append(ALPHA_NUMERIC_STRING.charAt(character));
    }
    return builder.toString();
}
    
