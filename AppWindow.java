    private void CheckInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckInButtonActionPerformed
        int option = JOptionPane.showConfirmDialog(CheckInPanel, "Are these details correct?");
        if(option==JOptionPane.YES_OPTION)
        {
            String name = NameField_CI.getText();
            int occ = Integer.parseInt(OccupantsComboBox.getSelectedItem().toString());
            int nights = Integer.parseInt(NightsComboBox.getSelectedItem().toString());
            HotelRoom.RoomType type = HotelRoom.RoomType.valueOf(RoomTypeComboBox.getSelectedItem().toString());
            int rNum = -1;
            HotelRoom room = null;
            if(!name.isBlank())
                rNum = GetRoomNumber(type);
            if (!ReservationCodeField.getText().isBlank())
                room = tempRoom;
            else
                room = new HotelRoom(name,occ,rNum,type,nights);
            if(rNum!=-999 && !name.isBlank())
            {
                roomsVec.add(room);
                if(!roomsVec.isEmpty()){
                    JOptionPane.showMessageDialog(CheckInPanel,"Check in successful!");
                    //reset all fields
                    NameField_CI.setText("");
                    NightsComboBox.setSelectedIndex(0);
                    RoomTypeComboBox.setSelectedIndex(0);
                    OccupantsComboBox.setSelectedIndex(0);
                    //update the cost label
                    Calculate_Update_Cost();
                    //checking if a reservation code was used
                    tempRoom = null;
                    room.setRCode("");
                }
            }
            else
            {
                if(rNum==-999)
                    JOptionPane.showMessageDialog(CheckInPanel,"No rooms of this type available!");
                if(name.isBlank() || rNum == -1)
                    JOptionPane.showMessageDialog(CheckInPanel,"Name can't be empty!");
            }
        }
        NameField_CI.setText("");
        ReservationCodeField.setText("");
    }//GEN-LAST:event_CheckInButtonActionPerformed

